/**
 * Download file from URL
 */
function downloadFile(url, filepath) {
    return new Promise((resolve, reject) => {
        const protocol = url.startsWith('https') ? https : http;
        const file = fs.createWriteStream(filepath);

        console.log(`Downloading ${url} to ${filepath}...`);

        protocol.get(url, (response) => {
            response.pipe(file);

            file.on('finish', () => {
                file.close();
                console.log(`âœ… Downloaded ${filepath}`);
                resolve(true);
            });
        }).on('error', (err) => {
            fs.unlink(filepath, () => {}); // Delete the file on error
            console.log(`âŒ Error downloading ${filepath}: ${err.message}`);
            reject(err);
        });
    });
}

/**
 * Read BTC historical data from CSV
 */
function readBtcData(filepath) {
    try {
        if (!fs.existsSync(filepath)) {
            console.log(`âŒ File not found: ${filepath}`);
            return [];
        }

        const fileContent = fs.readFileSync(filepath, 'utf-8');
        const records = parse(fileContent, {
            columns: true,
            skip_empty_lines: true
        });

        const btcData = [];
        for (const row of records) {
            // Handle potential # prefix in column names
            const date = (row['# date'] || row['date'] || '').replace('# ', '');
            const price = parseFloat(row['btc_price_usd'] || row['price'] || 0);

            if (date && price > 0) {
                btcData.push({
                    date: date,
                    price: price
                });
            }
        }

        console.log(`âœ… Read ${btcData.length} BTC price records`);
        return btcData;
    } catch (error) {
        console.log(`âŒ Error reading BTC data: ${error.message}`);
        return [];
    }
}

/**
 * Read MNAV data from CSV
 */
function readMnavData(filepath) {
    try {
        if (!fs.existsSync(filepath)) {
            console.log(`âŒ File not found: ${filepath}`);
            return [];
        }

        const fileContent = fs.readFileSync(filepath, 'utf-8');
        const records = parse(fileContent, {
            columns: true,
            skip_empty_lines: true
        });

        const mnavData = [];
        for (const row of records) {
            // Handle potential # prefix in column names
            const date = (row['# date'] || row['date'] || '').replace('# ', '');

            if (date) {
                mnavData.push({
                    date: date,
                    spotPrice: parseFloat(row['spot btc_price_usd'] || row['spotPrice'] || 0),
                    mnav: parseFloat(row['MNAV'] || row['mnav'] || 0),
                    mnavAdjustedPrice: parseFloat(row['MNAV_x_BTC_Price'] || row['mnavAdjustedPrice'] || 0),
                    btcHoldings: parseFloat(row['MSTR_BTC_Holdings'] || row['btcHoldings'] || 0),
                    marketCap: parseFloat(row['MSTR_Market_Cap_USD'] || row['marketCap'] || 0)
                });
            }
        }

        console.log(`âœ… Read ${mnavData.length} MNAV records`);
        return mnavData;
    } catch (error) {
        console.log(`âŒ Error reading MNAV data: ${error.message}`);
        return [];
    }
}

/**
 * Write data to data.js file
 */
function writeDataJs(btcData, mnavData, filepath) {
    try {
        const timestamp = new Date().toISOString();
        const jsContent = `// Bitcoin Rainbow Chart Data
// Last updated: ${timestamp}
// Generated by update-data.js

const btcHistoricalData = ${JSON.stringify(btcData, null, 2)};

const mnavHistoricalData = ${JSON.stringify(mnavData, null, 2)};
`;

        fs.writeFileSync(filepath, jsContent);

        console.log(`âœ… Written data.js with ${btcData.length} BTC records and ${mnavData.length} MNAV records`);
        if (btcData.length > 0) {
            console.log(`ğŸ“Š BTC data range: ${btcData[0].date} to ${btcData[btcData.length - 1].date}`);
        }
        if (mnavData.length > 0) {
            console.log(`ğŸ“Š MNAV data range: ${mnavData[0].date} to ${mnavData[mnavData.length - 1].date}`);
        }
        return true;
    } catch (error) {
        console.log(`âŒ Error writing data.js: ${error.message}`);
        return false;
    }
}

